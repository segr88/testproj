/* StartDefaultTask function */
/**
  * @brief  Thread function of main loop
  * @note   This function is needed for start TIMs, init UART-interrupt, 
  *         control of LED, output and calc AVG voltage, change TIM3 period 
  *         in eternal loop
  * @retval None
  */
void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  /* Infinite loop */
	HAL_UART_StateTypeDef uartstate;
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);   // start PWM timer
	HAL_TIM_Base_Start_IT(&htim3);              // start ADC-read timer
	flag_add_ADC_value = 0;
	while(1)
	{
		//check UART State and if it ready start receive next byte
		uartstate = HAL_UART_GetState(&huart1);	
		if (uartstate==HAL_UART_STATE_READY)
		{
			HAL_UART_Receive_IT(&huart1, &messRXbyte, 1);
		}
		//LED proc
		if (AL.Led) {HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_SET);}
		else HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_RESET);
		//out AGV
		if (AL.AVG_V_out) {
			{xQueueSendToBackFromISR(oqueue, AL.stravg, 0);}
			AL.AVG_V_out = 0;
		}
		//ADC AVG calc
		if (flag_add_ADC_value) {
			add_ADC_Value(&AL, ADC_Value);
			flag_add_ADC_value = 0;
		}
		//TIM3 change period
		if ((AL.Period>=10) && (AL.Period<=1000)){
			TIM3->ARR = AL.Period;
		}
	}
  /* USER CODE END 5 */ 
}

/* StartMessTask function */
/**
  * @brief  Thread function of message processing loop 
  * @note   Loop of waiting incoming message, call parsing 
  *         function ALdoit and UART output "OK", if OK 
  * @retval None
  */
void StartMessTask(void const * argument)
{
  /* USER CODE BEGIN StartMessTask */
  /* Infinite loop */
  uint8_t inmess_buf[S_IMESSAGE_BUFFER_SIZE];
	BaseType_t xStatus;
	while(1)
	{
		xStatus = xQueueReceive(iqueue, inmess_buf, 0);
		if(xStatus == pdPASS)
		{
			//mess proc
			if (ALdoit(&AL, (char *)inmess_buf)) 
				{xQueueSendToBackFromISR(oqueue, "OK\n\0", 0);}
		}
	}
  /* USER CODE END StartMessTask */
}

/* StartUARTTxTask function */
/**
  * @brief  Thread function of UART output loop
  * @note   Loop of waiting output messages from queue oqueue and
  *         transmit their to UART
  * @retval None
  */
void StartUARTTxTask(void const * argument)
{
  /* USER CODE BEGIN StartUARTTxTask */
  /* Infinite loop */
	uint8_t outmess_buf[S_OMESSAGE_BUFFER_SIZE];
	BaseType_t xStatus;
	int n;
	while(1)
	{
		xStatus = xQueueReceive(oqueue, outmess_buf, 0);
		if(xStatus == pdPASS)
		{
			for(n=1; (n<S_OMESSAGE_BUFFER_SIZE)&&(outmess_buf[n]!=0); n++);  //calculate length of string in outmessage_buf 
			HAL_UART_Transmit(&huart1, outmess_buf, n, 100);
		}
	}
  /* USER CODE END StartUARTTxTask */
}

