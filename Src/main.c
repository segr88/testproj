/**
  * @brief  Callback function of UART receive interrupt. 
  * @note   This function is called when on UART incomming new byte. 
  *         Incomming bytes accumulating to buffer until terminal null,  
	*					and further sending in queue for parsing. 
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if (messRXbyte == 0)
	{
		mess_buf[mess_byte_num] = 0;
		//put in queue
		if (xQueueSendToBackFromISR(iqueue, mess_buf, 0) != pdPASS)
				xQueueSendToBackFromISR(oqueue, "BUSY\n\0", 0);
		mess_byte_num = 0;
	}
	else
	if(mess_byte_num < S_IMESSAGE_SIZE) 
	{
		mess_buf[mess_byte_num++] = messRXbyte;
	}
	HAL_UART_Receive_IT(&huart1, &messRXbyte, 1);
}



/**
  * @brief  Callback function of ADC converting interrupt. 
  * @note   This function called of finish of converting ADC value. 
  *         Wherein stoping ADC, and write meansuring result to 
  *         TIM2 PWM-register with conversion from 12-bit to 16-bit.
  * @retval None
  */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	if(hadc->Instance == ADC1)
	{
		HAL_ADC_Stop_DMA(&hadc1);
		TIM2->CCR1 = ADC_Value << 4;   // from ADC to PWM (no the DAC on this MC, but another MC don't start on the keil simulator)
		flag_add_ADC_value = 1;
	}
}


